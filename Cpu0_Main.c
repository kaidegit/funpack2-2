#include "Ifx_Types.h"
#include "IfxCpu.h"
#include "IfxScuWdt.h"
#include "led.h"
#include "uart.h"
#include "adc.h"
#include "string.h"
#include "stdio.h"
#include "timer.h"
#include "pwm.h"
#include "math.h"

IfxCpu_syncEvent g_cpuSyncEvent = 0;

int core0_main(void)
{
    IfxCpu_enableInterrupts();
    
    /* !!WATCHDOG0 AND SAFETY WATCHDOG ARE DISABLED HERE!!
     * Enable the watchdogs and service them periodically if it is required
     */
    IfxScuWdt_disableCpuWatchdog(IfxScuWdt_getCpuWatchdogPassword());
    IfxScuWdt_disableSafetyWatchdog(IfxScuWdt_getSafetyWatchdogPassword());
    
    /* Wait for CPU sync event */
    IfxCpu_emitEvent(&g_cpuSyncEvent);
    IfxCpu_waitEvent(&g_cpuSyncEvent, 1);

    Uart_Init();
    Uart_SendStr("HelloWorld\r\n");
    Adc_Init();
    Adc_Run();
    Timer_Init();
    PWM_Init();

    uint32_t adc_val;
    uint8_t delay;
    uint16_t send_count = 0;
    char ch[32];
    while(1) {
        // adc value should be between 0 and 4095
        // 基准时间：adc_val = 2048， 呼吸灯周期2s，每个占空比延时10ms
        // 最短周期：adc_val = 0， 呼吸灯周期0.5s，每个占空比延时2.5ms
        // 最长周期：adc_val = 4095，呼吸灯周期8s，每个占空比延时40ms
        // 每个占空比延时t = 2.5 + adc_val
        // 500ms上报ADC数值
        for (uint8_t i = 0; i < 100; i++) {
            PWM_SetDuty(i);
            adc_val = Adc_GetValue();
            delay = 2.5 * pow(2, adc_val / 1024.0);
            Timer_Delayms(delay);
            send_count += delay;
            if (send_count >= 500) {
                send_count -= 500;
                sprintf(ch, "adc_val:%d\r\n", (int)adc_val);
                Uart_SendStr(ch);
            }
        }
        for (uint8_t i = 100; i > 0; i--) {
            PWM_SetDuty(i);
            delay = 2.5 * pow(2, adc_val / 1024.0);
            Timer_Delayms(delay);
            send_count += delay;
            if (send_count >= 500) {
                send_count -= 500;
                sprintf(ch, "adc_val:%d\r\n", (int)adc_val);
                Uart_SendStr(ch);
            }
        }
    }
    return (1);
}
